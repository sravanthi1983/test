package com.test;

public class forLoop {

	public static void main(String[] args) {
		
		int operation = 2;
		int number = 10;
		 
		switch (operation) {
		    case 1:
		        number = number + 10;
		        System.out.println("case1");
		        break;
		    case 2:
		        number = number - 4;
		        System.out.println("case2");
		        
		    case 3:
		        number = number / 3;
		        System.out.println("case3");
		        
		    case 4:
		        number = number * 10;
		        System.out.println("case4");
		        break;
	}

}
}
